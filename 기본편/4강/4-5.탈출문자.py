# \n : 줄바꿈 문자
print("백문이 불여일견 \n백견이 불여일타")

# 저는 "나도코딩"입니다, 탈출문자 \"\"
print("저는 '나도코딩'입니다.")
print('저는 "나도코딩"입니다.')
print("저는 \"나도코딩\"입니다.")

# \\ : 문장 내에서 \
print("c:\\user\\32195043")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rP{ine")

# \b : 백스페이스 (한글자삭제)
print("Rddd\b Apple")

# \t : 탭
print("Red\tapple")